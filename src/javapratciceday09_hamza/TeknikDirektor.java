package javapratciceday09_hamza;

public class TeknikDirektor {
	private String isim;
	private float maas;
	public TeknikDirektor(String isim, float maas) {
		super();
		this.isim = isim;
		this.maas = maas;
	}
	public String getIsim() {
		return isim;
	}
	public float getMaas() {
		return maas;
	}


}
